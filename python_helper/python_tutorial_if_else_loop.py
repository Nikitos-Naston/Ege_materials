# =============================================================================
# 1. УСЛОВНЫЕ ОПЕРАТОРЫ: if / elif / else
# =============================================================================
# Синтаксис:
# if условие:
#     блок         
# elif другое_условие:
#     блок
# else:
#     блок

# Операторы сравнения: ==, !=, >, <, >=, <=
# Логические: and, or, not

# Пример: знак числа
n = int(input("Введите число: "))
if n > 0:
    print("Положительное")
elif n == 0:
    print("Ноль")
else:
    print("Отрицательное")

# Пример: чёт/нечёт
n = int(input("Введите число: "))
if n % 2 == 0:
    print("Чётное")
else:
    print("Нечётное")

# Пример: составное условие
age = int(input("Возраст: "))
if 7 <= age <= 17:
    print("Школьник")
elif age < 7 or age > 65:
    print("Льготная категория")
else:
    print("Взрослый")


# =============================================================================
# 2. match-case 
# =============================================================================
# Аналог switch для дискретных вариантов
cmd = input("Команда (start/stop/help): ")
match cmd:
    case "start":
        print("Запуск")
    case "stop":
        print("Остановка")
    case "help":
        print("Помощь")
    case _:
        print("Неизвестно")

# =============================================================================
# 3. ЦИКЛ while
# =============================================================================
# Синтаксис: while условие: блок
# Используй, когда неизвестно заранее число повторений

# Счётчик:
i = 1
while i <= 5:
    print(i)
    i += 1

# Бесконечный цикл с выходом:
while True:
    cmd = input("> ")
    if cmd == "exit":
        break
    print("Вы ввели:", cmd)

# Пропуск итерации:
i = 0
while i < 10:
    i += 1
    if i % 2 == 0:
        continue
    print(i)  # печатает нечётные

# =============================================================================
# 4. ЦИКЛ for
# =============================================================================
# Итерация по диапазону/списку/строке

# Диапазоны:
for i in range(5):       # 0..4
    print(i)
for i in range(2, 7):    # 2..6
    print(i)
for i in range(10, 0, -2):  # 10,8,6,4,2
    print(i)

# По последовательности:
for ch in "Python":
    print(ch)


# break / continue:
for i in range(1, 100):
    if i*i > 50:
        break
    if i % 3 == 0:
        continue
    print(i)

# Вложенные циклы:
for i in range(1, 4):
    for j in range(1, 4):
        print(i, j)

# =============================================================================
# 5. КОМБИНАЦИЯ УСЛОВИЙ И ЦИКЛОВ
# =============================================================================

# Поиск первого делителя:
n = int(input("n: "))
found = False
for d in range(2, n):
    if n % d == 0:
        print("Делитель:", d)
        found = True
        break
if not found:
    print("Простое")

