# 5. АРИФМЕТИЧЕСКИЕ ОПЕРАЦИИ
# =============================================================================

"""
МАТЕМАТИЧЕСКИЕ ОПЕРАТОРЫ:

+   сложение
-   вычитание
*   умножение
/   деление (всегда возвращает float)
//  целочисленное деление (отбрасывает остаток)
%   остаток от деления
**  возведение в степень
"""

# Примеры:
a = 10
b = 3

print("Сложение:", a + b)  # 13
print("Вычитание:", a - b)  # 7
print("Умножение:", a * b)  # 30
print("Деление:", a / b)  # 3.333...
print("Целочисленное деление:", a // b)  # 3
print("Остаток от деления:", a % b)  # 1
print("Возведение в степень:", a ** b)  # 1000

# Проверка чётности числа:
number = 8
if number % 2 == 0:
    print(number, "- чётное число")


